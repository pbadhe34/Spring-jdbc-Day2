 
public class User {
 private int num;
 private String name;
 private float income; 
 
 
public int getNum() {
	return num;
}
public void setNum(int num) {
	this.num = num;
}
public String getname() {
	return name;
}
public void setname(String name) {
	this.name = name;
}
public float getIncome() {
	return income;
}
public void setIncome(float income) {
	this.income = income;
}
}
